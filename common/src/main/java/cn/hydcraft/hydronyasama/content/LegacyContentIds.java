  private static final List<String> BATCH2_STANDARD_SUFFIXES =
      Collections.unmodifiableList(
          Arrays.asList(
              "_carpet", "_edge", "_pane", "_railing", "_roof", "_strip", "_vslab", "_vstrip"));

  private static final List<String> BATCH2_COMPACT_SUFFIXES =
      Collections.unmodifiableList(Arrays.asList("_carpet", "_pane"));

  public static final List<String> BUILDING_BATCH2_DERIVED_BLOCK_IDS =
      Collections.unmodifiableList(buildingBatch2DerivedBlockIds());

  private static List<String> buildingBatch2DerivedBlockIds() {
    List<String> ids = new ArrayList<>();
    for (String baseId : BUILDING_BLOCK_IDS) {
      if (baseId.endsWith("_logo") || baseId.endsWith("_sign")) {
        continue;
      }
      String prefix = stripBlockSuffix(baseId);
      List<String> suffixes =
          isCompactFamilyBase(baseId) ? BATCH2_COMPACT_SUFFIXES : BATCH2_STANDARD_SUFFIXES;
      for (String suffix : suffixes) {
        ids.add(prefix + suffix);
      }
    }
    ids.add("nsb_nsdn");
    return ids;
  }

  private static String stripBlockSuffix(String id) {
    return id.endsWith("_block") ? id.substring(0, id.length() - "_block".length()) : id;
  }

  private static boolean isCompactFamilyBase(String id) {
    return id.contains("mesh_wire")
        || id.equals("square_iron_mesh_block")
        || id.startsWith("tarp_")
        || id.equals("tatami_block");
  }

